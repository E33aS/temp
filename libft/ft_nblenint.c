#include "libft.h"

int		ft_nblenint(int nb)
{
	int				len;
	unsigned int	n;

	len = 0;
	if (nb < 0)
	{
		nb = -nb;
		len++;
	}
	n = nb;
	while (n > 9)
	{
		n /= 10;
		len++;
	}
	len++;
	return (len);
}
